    private void txtBetAmtFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtBetAmtFocusLost
        // TODO add your handling code here:
        //logic that gets triggered, when user inputs Bet Amount and comes out of the text field
        //Logic to check whether Bet Amount is more than Balance amount or Bet amount is negative. If YES, will display error message and discard the input
        if (Round_Count == 0)
        {
             Bet_Amt = Integer.parseInt(txtBetAmt.getText());
             if (Bet_Amt > User_Bal_Amt)
             {
                 lblBetErrorMsg.setText("ERROR. Your BET Amount cannot exceed your Balance Amount");
                 txtBetAmt.setText("0");
                 Bet_Amt = 0;
             }
             else if (Bet_Amt < 0)
                 {
                 lblBetErrorMsg.setText("ERROR. Bad input on the Bet Amount");
                 txtBetAmt.setText("0");
                 Bet_Amt = 0; 
                 }
             else
             {        
                 lblBetErrorMsg.setText("");
             }
        }
        else
        {
             lblBetErrorMsg.setText("ERROR. You cannot change the Bet in the middle of the Round.");
             txtBetAmt.setText(String.valueOf(Bet_Amt));
        }
    }//GEN-LAST:event_txtBetAmtFocusLost

    private void txtUserGuessNumFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtUserGuessNumFocusLost
        // TODO add your handling code here:
        //logic that gets triggered, when user inputs Guess Number and comes out of the text field
        //Logic to check whether Guess Number is between 1 and 6. If NOT, will display error message and discard the input
        lblBetErrorMsg.setText("");
        User_Guess_Num = Integer.parseInt(txtUserGuessNum.getText());
        if ((User_Guess_Num <= 0) || (User_Guess_Num > 6))
        {
            txtUserGuessNum.setText("0");
            User_Guess_Num = 0;
            lblRoundResultTxt.setText("ERROR USER INPUT. You need to choose between 1 and 6");
        }
        else
        {
            lblRoundResultTxt.setText("");
        }
    }//GEN-LAST:event_txtUserGuessNumFocusLost

