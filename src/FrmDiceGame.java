    private void btnRollDiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRollDiceActionPerformed
        // TODO add your handling code here:
        //logic that gets triggered, when ROLL DICE button is clicked
        //Generates the random number between 1 and 6, when user clicks Roll Dice. 
        //If User Guess is same as Computer generated number, it is a Correct Guess. Else, Missed count will be incremented.
        if (User_Guess_Num > 0)
        {
            btnContinue.setText("CONTINUE");
            btnPlaceNewBet.setText("");
            btnEndGame.setText("");
            btnRestart.setText("");
            User_Guess_Num = Integer.parseInt(txtUserGuessNum.getText());
            Comp_Roll_Num = (int)Math.round(Math.random()*5+1);
            lblCompRollNum.setText(String.format(""+Comp_Roll_Num));
            Round_Count = Round_Count + 1;
            Total_Rolls_Count = Total_Rolls_Count + 1;
        
            if (User_Guess_Num == Comp_Roll_Num)
            {
                Round_Success_Count = Round_Success_Count + 1;
                Total_Success_Count = Total_Success_Count + 1;
                lblRoundResultTxt.setText("Good Guess. You WON. Keep Going");
            }
            else
            {
                Round_Missed_Count = Round_Missed_Count + 1;
                Total_Miss_Count = Total_Miss_Count + 1;
                lblRoundResultTxt.setText("Wrong Guess. You LOST. Try again");
            }    
            if (Round_Count == 3)
            {
                btnContinue.setText("RESULT");
            }
        }
 
    }//GEN-LAST:event_btnRollDiceActionPerformed

    private void btnEndGameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEndGameActionPerformed
        // TODO add your handling code here:
        //logic that gets triggered, when END GAME button is clicked
        //Generates the statistics, to include Total number of dice rolled, total correct guess, total incorrect guess and percentage calculated limiting to 2 decimals. 
        lblStatsTitle.setText("GAME STATISTICS");
        lblTotalRollsTxt.setText("Total Number of Rolls                                     :  ");
        lblTotalSuccessTxt.setText("Number of CORRECT Guess you made       :  ");
        lblTotalMissTxt.setText("Number of INCORRECT Guess you made  :  ");
        lblTotalRollsCount.setText(String.valueOf(Total_Rolls_Count));
        lblTotalSuccessCount.setText(String.valueOf(Total_Success_Count));
        lblTotalMissCount.setText(String.valueOf(Total_Miss_Count));
        if (Total_Rolls_Count > 0)
        {
           Total_Success_Perc = ((double)Total_Success_Count / (double)Total_Rolls_Count)*100;
           lblTotalSuccessPerc.setText(String.format("%.2f",Total_Success_Perc)+" %");
           Total_Miss_Perc = ((double)Total_Miss_Count / (double)Total_Rolls_Count)*100;
           lblTotalMissPerc.setText(String.format("%.2f",Total_Miss_Perc)+" %");
        }
        else
        {
           Total_Success_Perc = 0;
           lblTotalSuccessPerc.setText(String.format("%.2f",Total_Success_Perc)+" %");
           Total_Miss_Perc = 0;
           lblTotalMissPerc.setText(String.format("%.2f",Total_Miss_Perc)+" %");
        }
        
        //Display final message depending on the Loss or Gain 
        if (User_Bal_Amt == USER_BAL_AMT_LMT)
        {
           lblWinLossDesc.setText("No Loss - No Gain. Your Current Balance is $ " + User_Bal_Amt);
        }
        else if (User_Bal_Amt > USER_BAL_AMT_LMT)
        {
            User_Bal_Diff = User_Bal_Amt - USER_BAL_AMT_LMT;
            lblWinLossDesc.setText("Congrats. You made a profit of $ " + User_Bal_Diff + ".   Your Current Balance is $ " + User_Bal_Amt);
        }
        else
        {
            User_Bal_Diff = USER_BAL_AMT_LMT - User_Bal_Amt;
            lblWinLossDesc.setText("Sorry. You made a loss of $ " + User_Bal_Diff + ".   Your Current Balance is $ " + User_Bal_Amt);
        }
    }//GEN-LAST:event_btnEndGameActionPerformed

