    private void btnContinueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinueActionPerformed
        // TODO add your handling code here: 

        //logic that gets triggered, when CONTINUE button is clicked
        //setting up the label text messages, depending on the round count 
        if (Round_Count < GAMES_PER_ROUND)
        {
            if (Round_Count == 1)
            {
                lblRoundTxt.setText("Round 2 of 3 :  ");
            }
            else if (Round_Count == 2)
            {
                lblRoundTxt.setText("Round 3 of 3 :  ");
            }
            lblCompRollNum.setText("");
            txtUserGuessNum.setText("0");
            lblRoundResultTxt.setText("");
        }
        else
        {
            //Adjusting the User Balance amount and Computer Balance amount, depending on the user winning or losing the game 
           if (Round_Success_Count >= 2)
           {
               lblRoundResultTxt.setText("Success BETTING. You have Won $ "+Bet_Amt);
               User_Bal_Amt = User_Bal_Amt + Bet_Amt;
               Comp_Bal_Amt = Comp_Bal_Amt - Bet_Amt;
           }
           else
           {
               lblRoundResultTxt.setText("Failure BETTING. You have Lost $ "+Bet_Amt);
               User_Bal_Amt = User_Bal_Amt - Bet_Amt;
               Comp_Bal_Amt = Comp_Bal_Amt + Bet_Amt;
           }
           lblUserBalAmt.setText(String.format("$ "+User_Bal_Amt));
           lblCompBalAmt.setText(String.format("$ "+ Comp_Bal_Amt));
           btnContinue.setText("");
           btnPlaceNewBet.setText("Place New Bet");
           btnEndGame.setText("End Game");
           btnRestart.setText("RESTART");
        }
    }//GEN-LAST:event_btnContinueActionPerformed

